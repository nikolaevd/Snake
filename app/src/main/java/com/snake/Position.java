
package com.snake;

public class Position {

    private int posByX;
    private int posByY;

    Position(int posByX, int posByY) {

        this.posByX = posByX;
        this.posByY = posByY;

    }

    public int getPosByX() {
        return posByX;
    }

    public int getPosByY() {
        return posByY;
    }

}
